package javaEski;

public class LoopDemo {

	public static void main(String[] args) {
		// for döngüsü
		for (int i = 1; i < 10; i++) {
			System.out.println(i);
		}
		System.out.println("döngü bitti");

		// While döngüsü
		int i = 1;
		while (i < 10) {
			System.out.println(i);
			i++;
		}
		System.out.println("While Döngüsü Bitti");
		
	}

}
